    public function countByDay()
    {
        $date = new DateTime();
        $date = $date->format('Y-m-d');
        try {
            return $this->createQueryBuilder('n')

                ->andWhere('n.date = :today')
                ->setParameter('today', $date )
                ->select('COUNT(n)')
                ->getQuery()
                ->getSingleScalarResult();
        } catch (NoResultException | NonUniqueResultException $e) {
        }
    }

